const SVG_RULES = `## 输出格式
只输出一个完整 SVG，不要 Markdown 代码块，不要解释文字。
根节点必须完全符合：
<svg xmlns="http://www.w3.org/2000/svg" width="1280" height="720" viewBox="0 0 1280 720">
...
</svg>

## 允许的 SVG 元素
- 优先使用：<svg>、<g>、<rect>、<circle>、<ellipse>、<line>、<text>、<tspan>
- 可以使用少量装饰性 <path>、<polygon>、<polyline> 来做箭头、折线、波形、抽象背景和数据图形，但不能用它们承载正文
- 禁止使用：<style>、class、foreignObject、image、defs、marker、mask、clipPath、filter、linearGradient、radialGradient、script、iframe
- 不要使用 rgba()；透明度用 fill-opacity / stroke-opacity
- 不要使用 HTML 实体如 &nbsp;、&mdash;、&copy;；排版符号直接用 Unicode；文本里的 & < > 必须写成 &amp; &lt; &gt;

## PPTX 可编辑约束
- 每个文字块必须是独立 <text>，不要把图标和文字塞进同一个元素
- 每个 <text> 必须带 data-w 和 data-h，表示文本框宽高，例如：
  <text x="80" y="130" data-w="720" data-h="64" font-size="36" font-family="Microsoft YaHei" font-weight="700" fill="#111827">标题</text>
- data-w / data-h 必须直接写在 <text> 上，不要只写在 <tspan> 上；短标签、页码、小注释也不能省略
- data-w / data-h 是真实 PPT 文本框边界：start 文本满足 x+data-w<=1224；middle 文本满足 x-data-w/2>=56 且 x+data-w/2<=1224；end 文本满足 x-data-w>=56
- 多行文字必须用 <tspan x="..." dy="..."> 手动换行；每行不要超过文本框宽度
- 中文字体统一用 Microsoft YaHei；英文数字可用 Arial
- 标题 42-58px，副标题 22-30px，模块标题 26-36px，重点数字/关键词 30-44px，正文 17-23px，注释 12-16px；不要小于 11px
- 不要使用 transform 旋转或缩放；如需分组，仅使用 <g id="...">
- 文字、卡片、线条、圆形、矩形必须保持原生可编辑；复杂装饰如果无法编辑也不能影响主体内容可编辑

## 安全区与排版
- 画布固定 1280×720；所有可见内容必须在 x=56..1224、y=42..674 内
- 饱满不等于放大到出血；所有标题、正文、页码、贴纸文字的文本框都必须完整落在安全区内
- 标题区 y=54..150；主体区 y=170..620；页脚或页码 y=650..674
- 卡片内边距至少 22px；文字与边框至少 18px；相邻卡片间距至少 16px
- 不允许文字重叠、文字压在图形边框上、文字超出卡片、正文贴边
- 如果内容很多，优先删减和分组，不要硬塞；最多 3 层信息层级
- 图案和贴纸可以醒目，但不能遮挡文字或抢走主结论；它们必须由基础 SVG 图形组成，不要用图片或图标字体

## 视觉要求
- 先根据 visual_type 选择结构：对比矩阵、流程、时间线、数据卡、象限、卡片网格、大图注释的替代结构等
- 按策划稿意图自由创作 SVG：可以做情境插画、流程场景、贴纸标签、手绘感标注、对比装置、地图/清单/仪表/便签等原生矢量画面
- 不要默认做空洞表格或大留白卡片；表格、列表只在它确实最适合表达时使用，并配合重点标签、视觉锚点或小场景增强表达
- 画面要专业、有趣、饱满，留白必须有意图；避免主体偏左、右侧空洞、只有一点内容撑不起整页
- 默认使用浅色/白色背景，不要强制深色背景；重点学习高质量参考稿的排版、色彩层级和信息密度
- 颜色要丰富但有秩序：每页使用 1 个主色、2-4 个高饱和强调色和浅色层次底色；不要只有灰白绿淡色
- 用字号、字重和颜色明确突出重点：标题大而有力，关键词/数字/结论用更大字号或强调色，辅助文字明显弱化
- 允许用大色块、色带表头、交替行底色、细网格、轴线、圆环、数字仪表、对比表、流程箭头、半透明背景层、装饰性几何、贴纸徽章来增强视觉，不要做得单调
- 贴纸/图标/视觉锚点要醒目，常规尺寸 64-140px；不要只画很小的点状图标凑数
- 使用统一配色，但每页可有不同节奏；背景、标题、重点色、卡片边界要清楚
- 给主要内容块加 <g id="title">、<g id="content">、<g id="cards">、<g id="chart"> 等顶层分组，方便后续导出和调试

## 表格与矩阵
- 表格/矩阵必须像设计作品而不是默认表格：满宽主体、整齐列宽、强色块表头、交替行底色、列/行重点色分区
- 单元格不要空洞：用大号关键词、彩色标签、等级徽章、短说明形成层级；同一行的文字基线和居中方式必须一致
- 不要出现多余空列、断线、错位或右侧大片空白；表格主体建议覆盖安全区宽度的 78%-90%

## 图文绑定、避让与层级
- 每个内容模块先确定容器 x/y/w/h，再在容器内安排图标、标题、正文、标签；模块之间保持 18-28px 清晰间距，除背景装饰外不要叠放
- 绘制顺序固定：背景装饰 -> 网格/连接线/图表底层 -> 卡片/数据图形 -> 标题/正文/标签/页码；后绘制的装饰不能压住内容
- 图标/贴纸必须和对应文字形成一个视觉组，通常在标题上方或左侧，与文字至少间隔 16px；不要让文字脱离所属卡片
- 连接线、箭头、徽章只走模块间空隙或边缘；碰到卡片、图表、文字时绕开或断开，不要穿过内容主体
- 图表先预留标签轨道：柱形图数值/标签在柱体外侧或顶部留白中，坐标轴文字在独立轴标签区，不能压住柱体、轴线或说明文字
- 主标题、副标题、页脚、软件标签、推荐条都要先预留固定区域；不要贴边，也不要覆盖主内容区

## 构图密度与视觉平衡
- 输出 SVG 前先在心里完成布局规划：标题区、主体网格、页脚、每个模块的 x/y/w/h；不要输出规划文字
- 主体内容不能只堆在左半屏；除封面和章节页外，主要内容组应横向覆盖画布约 70%-88%，视觉重心接近 x=640
- 左右留白要有意图且基本均衡；如果采用左图右文/左右栏，右侧必须有等量信息块、图表、注释或视觉锚点
- 卡片尺寸由内容决定：只有图标、标题和一两行说明时，不要使用过高大卡片；卡片内部有效内容应占高度约 55%-80%
- 大容器里不能只有少量小字或小图标；如果容器很大，要补充层级、标签、分隔线、数据点或缩小容器
- 每页至少有 1 个明确视觉主角或叙事装置，2-4 个信息块，2-6 个不遮挡内容的轻量装饰/贴纸/标注
- 输出前自检：画面是否饱满、左右是否平衡、文本框是否全在安全区、文字是否互相遮挡；不满足就重新排版再输出`;

